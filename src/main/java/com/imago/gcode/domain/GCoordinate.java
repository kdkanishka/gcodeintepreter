package com.imago.gcode.domain;

/**
 *
 * @author kanishka
 */
public class GCoordinate {

    private double x, y;

    public GCoordinate(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
